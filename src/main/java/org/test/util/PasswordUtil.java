package org.test.util;

import org.mindrot.jbcrypt.BCrypt;

/**
 * 密码哈希工具 —— 用 bcrypt 算法保护用户密码。
 *
 * ============ 为什么不能明文存密码？ ============
 *
 * 如果数据库被攻击者拿到（比如备份文件泄露、SQL 注入），
 * 明文密码会直接暴露所有用户的登录凭证。
 * 很多人习惯在不同网站用同一个密码，一个站点的泄露
 * 会导致用户在其他网站（银行、邮箱……）的账号也被盗。
 *
 * 所以绝不能存明文，必须存"哈希"。
 *
 *
 * ============ 什么是哈希？ ============
 *
 * 哈希（Hash）是把任意文字变成一段固定长度的"乱码"的数学运算。
 * 比如 "hello" 经过 bcrypt 会变成类似：
 * "$2a$10$N9qo8uLOickgx2ZMRZoMyeIjZAgcfl7p92ldGxad68LJZdL17lhWy"
 *
 * 哈希有三个关键特性：
 * 1. 单向性 —— 从哈希值无法反推出原始密码。
 *    这就像"把肉做成肉松"，做成肉松后回不到原来的肉。
 * 2. 确定性 —— 同样的输入永远得到同样的输出。
 * 3. 雪崩效应 —— 输入改一丁点，输出完全变样。
 *    "hello" 和 "hallo" 的哈希值天差地别，无法通过相似度猜测。
 *
 *
 * ============ 什么是盐值（Salt）？ ============
 *
 * 如果只用哈希，相同的密码会产生相同的哈希值。
 * 那攻击者可以提前算好常见密码的哈希（彩虹表），
 * 拿到数据库后直接对照就能查出明文。
 *
 * 盐值是一个随机字符串，每个用户都不一样。
 * 哈希时把"明文密码 + 随机盐值"混在一起算。
 * 这样即使两个用户用完全相同的密码，
 * 数据库里存的值也完全不同。
 *
 * bcrypt 的好处：盐值自动生成，并嵌入到最终结果里。
 * 也就是说同一个字符串"存盐值 → 哈希 → 验证"全自动，
 * 不需要额外维护一张"盐值表"。
 *
 *
 * ============ 什么是工作因子（Work Factor）？ ============
 *
 * bcrypt 的第二个参数叫 rounds（轮数），默认是 10。
 * 意思是 2^10 = 1024 轮哈希迭代。
 *
 * 为什么要迭代多次？
 * 单次哈希太快了，攻击者用 GPU 一秒钟能试几十亿个密码。
 * 多次迭代人为"拖慢"这个过程，每次登录慢 0.1 秒
 * 对用户体验没影响，但对暴力破解来说是灭顶之灾。
 *
 * 为什么是 10？
 * 这是一个经验值，平衡了安全性和性能。
 * 数值越大越安全，但登录也会越慢。
 *
 *
 * ============ 为什么这个类的方法都是 static？ ============
 *
 * 哈希操作是纯数学计算，不依赖任何状态。
 * 不需要 new 对象，直接调用就行。
 */
public class PasswordUtil {

    // 构造方法私有：纯工具类不应该被实例化
    private PasswordUtil() {
    }

    /**
     * 对明文密码做 bcrypt 哈希。
     *
     * 返回的字符串可以直接存入数据库。
     * bcrypt 会把盐值和工作因子都编码进结果中，
     * 所以不需要额外存储盐值 —— 验证时自动从结果中提取。
     *
     * 结果示例：
     * $2a$10$N9qo8uLOickgx2ZMRZoMyeIjZAgcfl7p92ldGxad68LJZdL17lhWy
     *  ├─┘ ├┘ └─────────── 22字符盐值 ──────────┘└──── 31字符哈希 ────┘
     *  │    └─ rounds=10（工作因子）
     *  └─ bcrypt 算法版本
     *
     * @param plainPassword 用户输入的明文密码
     * @return bcrypt 哈希字符串（固定 60 字符）
     */
    public static String hash(String plainPassword) {
        // BCrypt.gensalt() 生成一个随机盐值，默认 rounds=10
        // BCrypt.hashpw() 把密码和盐值一起做哈希
        return BCrypt.hashpw(plainPassword, BCrypt.gensalt());
    }

    /**
     * 验证明文密码和数据库中的哈希是否匹配。
     *
     * 这个操作做了什么（用白话讲）：
     * 1. 从哈希值中提取盐值（前面 29 个字符）
     * 2. 用同样的盐值和 rounds 重新算一遍哈希
     * 3. 比较两个哈希是否一致
     *
     * 注意：验证时不需要知道原始密码！
     * 这就是哈希的妙处 —— 能验证"对不对"而不需要知道"是什么"。
     *
     * @param plainPassword 用户登录时输入的密码
     * @param hash          数据库中存储的 bcrypt 哈希
     * @return true 表示密码正确，false 表示不对
     */
    public static boolean verify(String plainPassword, String hash) {
        // BCrypt.checkpw 内部会自动从 hash 中提取盐值和 rounds
        return BCrypt.checkpw(plainPassword, hash);
    }
}
